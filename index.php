<script>
    function activateTab(index) {
        var tabs = document.querySelectorAll(
            '.cont3 .inner .contents .box .rig .tabWrap .list .tab_item'
        );
        var tabContents = document.querySelectorAll(
            '.cont3 .inner .contents .box .rig .tabWrap .tab_inner_wrap .tab_inner'
        );

        tabs.forEach(function (tab) {
            tab
                .classList
                .remove('active');
        });
        tabContents.forEach(function (tabContent) {
            tabContent
                .classList
                .remove('active');
        });

        tabs[index]
            .classList
            .add('active');
        tabContents[index]
            .classList
            .add('active');
    }
</script>
